// JavaScript String Methods

const str = "Hello World";

str.length; // 11

str.charAt(4); // o

str.endsWith("d"); // true

str.includes("World"); // true

str.indexOf("W"); // 6

str.repeat(2);// "Hellow WorldHello World";

str.replace("World", "Marco"); // Hello Marco;

str.slice(6, 10); // "Worl"

